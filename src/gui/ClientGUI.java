    private void jButton_grabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_grabarActionPerformed

        if(jTextField_Dni_ruc.getText().equals("")||
            jTextField_Nombre.getText().equals("")||
            jTextField_telefono.getText().equals("")||
            jTextField_direccion. getText().equals("")||
            jTextField_origen.getText().equals("")
        ){
            JOptionPane.showMessageDialog(null, "Ingrese los datos completos");
        }else{

            Cliente c = new Cliente();
            c.setCliente_dni_ruc(jTextField_Dni_ruc.getText());
            c.setCliente_nombres(jTextField_Nombre.getText());
            c.setCliente_telefono(jTextField_telefono.getText());
            c.setCliente_direccion(jTextField_direccion.getText());
            c.setCliente_origen(jTextField_origen.getText());

            ic.registrarCliente(c);

            JOptionPane.showMessageDialog(null, "Se registro de manera satisfactoria");
            AddOneReporteData();
            limpiarJTextField();
        }
    }//GEN-LAST:event_jButton_grabarActionPerformed

