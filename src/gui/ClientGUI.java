    public void AddOneReporteData(){
        Object[] fila = new Object[6];
        
        fila[0] = jTextField_id.getText();
        fila[1] = jTextField_Dni_ruc.getText();
        fila[2] = jTextField_Nombre.getText();
        fila[3] = jTextField_telefono.getText();
        fila[4] = jTextField_direccion.getText();
        fila[5] = jTextField_origen.getText();
        
        modelo1.addRow(fila);
        jTableCliente.setModel(modelo1);
                
    }
        public void updateOneReporteData(int rowSelected){
            //Object[] fila = new Object[6];
            jTableCliente.setValueAt(jTextField_id.getText(), rowSelected, 0);
            jTableCliente.setValueAt(jTextField_Dni_ruc.getText(), rowSelected, 1);
            jTableCliente.setValueAt(jTextField_Nombre.getText(), rowSelected, 2);
            jTableCliente.setValueAt(jTextField_telefono.getText(), rowSelected, 3);
            jTableCliente.setValueAt(jTextField_direccion.getText(), rowSelected, 4);
            jTableCliente.setValueAt(jTextField_origen.getText(), rowSelected, 5);  
    }
        

    public void ReporteDataCT(){
        System.out.print("tablaCliente");
        
        modelo1 = new DefaultTableModel();
        modelo1.addColumn("ID");
        modelo1.addColumn("Dni/Ruc");
        modelo1.addColumn("Nombre");
        modelo1.addColumn("Telefono");
        modelo1.addColumn("Direccion");
        modelo1.addColumn("Origen");
     
        for (Cliente pers : ic.reporteCliente()) {
            Object[] fila = new Object[6];
            fila[0] = pers.getCliente_id();
            fila[1] = pers.getCliente_dni_ruc();
            fila[2] = pers.getCliente_nombres();
            fila[3] = pers.getCliente_telefono();
            fila[4] = pers.getCliente_direccion();
            fila[5] = pers.getCliente_origen();

            modelo1.addRow(fila);
        }
        jTableCliente.setModel(modelo1);
    }
